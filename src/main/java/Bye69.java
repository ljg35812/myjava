import java.util.ArrayList;
import java.util.List;

public class Bye69 {
    public static void main(String[] args) {
        List<Integer> ls = new ArrayList<>();

        for (int i = 0; i < 5; i++) {
            ls.add(i + 1);
        }

        for (int a : ls) {
            System.out.print(a + " ");
        }
    }
}
