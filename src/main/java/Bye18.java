public class Bye18 {
    public static void main(String[] args) {

        for (int i = 2; i <=9; i++) {
            System.out.println("___________");
            for (int j = 1; j <=9; j++) {
                int a = i*j;
                System.out.println(i + " x " + j + " = " + a);
            }
        }
        System.out.println();
    }
}
