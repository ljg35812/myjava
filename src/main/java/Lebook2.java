public class Lebook2 {
    public static void main(String[] args) {
        double a, b, c, g;

        int d = 3;
        int e = 2;
        int f = 1;

        a = 3.5;
        b = 4.0;
        c = 4.5;

        g = ((d * a) + (e * b) + (f * c)) / (d + e + f);
        System.out.println("평균학점: " + g);
    }
}
